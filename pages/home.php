<p><a target="_blank" href="https://owasp.org">Open Web Application Security Project</a> je projekt zabývající se bezpečností webových aplikací. Řeší takové věci jako nejvyskytovanější webové zranitelnosti, obrany proti nim. Spravují také projekt <a target="_blank" href="https://cheatsheetseries.owasp.org/index.html">cheatsheets</a>, kde je veškerá znalost o webové bezpečnosti zkompilovaná a veřejně dostupná.</p>

