<h2>Vítejte ve Vuln webové aplikaci</h2>

<p>Tato aplikace má za účel představit mnohé webové zranitelnosti. Na stránky jednotlivých zranitelností se dá dostat přes menu nahoře (odkazy na zranitelnosti jsou v první části navigace, ve druhé jsou stránky pro účely exploitace).</p>

<p>Doporučuji začít od LFI a dopracovat se až nakonec. Každá stránka obsahuje popis zranitelnosti i kroky, které je potřeba podniknout k jejímu zneužití. A to nejlepší je, že pro exploitaci nemusíte hledat jiné stránky. Tato aplikace je v určitých částech sama a schválně zranitelná.</p>

<h3>O web hackingu</h3>

<p>Webové aplikace jsou všude kolem nás, je jen přirozené, že mají svou vlastní snůšku zranitelností. Proč by nám ale měly vadit zrovna zde?</p>

<p>Odpověď je jednoduchá, webové aplikace jsou velmi často přístupné do internetu a tak mohou být dost možná právě místem, kde útočník prolomí náš perimetr a dostane se nám do sítě. Další důvod je, že se webové stránky využívají pro hosting služeb, na kterých závisí něčí obchod (kdybych se dostal na webserver amazonu a smazal ho, amazon by z toho měl neskutečnou ztrátu).</p>

<h3>OWASP</h3>

