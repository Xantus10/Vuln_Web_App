<h2>Welcome to the Vuln web app</h2>

<p>This app was created to teach about web application vulnerabilities. You may access the page of each vulnerability using the menu above (the first part of navigation are pages of vulnerabilities, the second are pages to be exploited).</p>

<p>I recommend starting with LFI and work your way to the end. Each page contains a description and explanation of the vulnerability as well as steps needed to exploit the vulnerability. And the best part is that you don't have to look for some vulnerable web pages elswhere. This application features vulnerable parts where you can exploit it.</p>

<h3>About web hacking</h3>

<p>Web applications are everywhere. It is only natural that they got their own bucket of vulnerabilities. But why should we care about them?</p>

<p>The answer is pretty simple. Web applications are more often than not exposed to the internet, so they can serve as the entrypoint to our network for an external attacker. Another reason is that web pages are used for providing certain services (if I hacked the web server of amazon and deleted it, amazon would suffer immense financial loss).</p>

